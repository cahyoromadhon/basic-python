# Input Value
alas = float(input("Masukkan Nilai Alas:  "))
tinggi = float(input("Masukkan Nilai Tinggi:  "))

# Calculate the area
area = 0.5 * alas * tinggi

# Display
print(f"Luas Segitiganya adalah: {area}")

